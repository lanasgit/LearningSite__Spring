package model;

public class ScategoryTO {
	private String seq;
	private String name;
	private String lseq;
	
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getLseq() {
		return lseq;
	}
	public void setLseq(String lseq) {
		this.lseq = lseq;
	}

}
